#include "main.h"

/**
 * add -sums two numbers
 * Return: the sum
 */
int sum(int a, int b)
{
	return (a + b);
}

/**
 * sub - finds difference
 * Return: the difference
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - finds product
 * Return: the product
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - finds division
 * Return: the division
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - finds the modulus
 * Return: the remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
